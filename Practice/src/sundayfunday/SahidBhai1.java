package sundayfunday;

public class SahidBhai1 {

	public static void main(String[] args) {

		int n=7;
		int fact=1;
		for(int i=1;i<=n;i++)
		{
			fact=fact*i;
		}
		//System.out.println(fact);
		int l2d=fact%100;
		if(l2d%7==0)
		{
			System.out.println("Divisible");
		}
		else
		{
			System.out.println("Not divisible.");
		}
	}

}
